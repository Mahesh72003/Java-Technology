package  com.xworkz.methodoverride6;

public class Device {

    public void start() {
        System.out.println("Device is starting up.");
    }

    public void shutdown() {
        System.out.println("Device is shutting down.");
    }

    public void performTask() {
        System.out.println("Device is performing its assigned task.");
    }

    public void update() {
        System.out.println("Device is updating to the latest software version.");
    }
}