package com.xworkz.methodoverride18;

public class Designer {

    public void design() {
        System.out.println("Designer is creating a new concept layout.");
    }

    public void plan() {
        System.out.println("Designer is planning the aesthetics and structure.");
    }

    public void review() {
        System.out.println("Designer is reviewing the final draft.");
    }

    public void present() {
        System.out.println("Designer is presenting the design to the client.");
    }
}