package com.xworkz.methodoverride16;

public class Fruit {

    public void grow() {
        System.out.println("Fruit is growing on the tree.");
    }

    public void harvest() {
        System.out.println("Fruit is being harvested.");
    }

    public void eat() {
        System.out.println("Fruit is being eaten for nutrition.");
    }

    public void store() {
        System.out.println("Fruit is stored in a cool, dry place.");
    }
}