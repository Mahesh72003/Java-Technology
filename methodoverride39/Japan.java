package com.xworkz.methodoverride39;

public class Japan extends Nation {

    @Override
    public void culture() {
        System.out.println("Japan has a unique and ancient culture with a mix of traditional and modern aspects.");
    }

    @Override
    public void population() {
        System.out.println("Japan has a population of approximately 126 million people.");
    }

    @Override
    public void geography() {
        System.out.println("Japan is an island nation located in East Asia, surrounded by the Pacific Ocean.");
    }

    @Override
    public void economy() {
        System.out.println("Japan has the third-largest economy in the world, with a strong focus on technology and innovation.");
    }
}