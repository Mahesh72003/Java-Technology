package com.xworkz.methodoverride10;

public class Instrument {

    public void play() {
        System.out.println("Instrument is being played.");
    }

    public void tune() {
        System.out.println("Instrument is being tuned.");
    }

    public void clean() {
        System.out.println("Instrument is being cleaned.");
    }

    public void store() {
        System.out.println("Instrument is being stored safely.");
    }
}