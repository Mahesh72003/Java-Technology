package com.xworkz.methodoverride20;

public class Machine {

    public void start() {
        System.out.println("Machine is starting its operation.");
    }

    public void stop() {
        System.out.println("Machine has stopped running.");
    }

    public void maintain() {
        System.out.println("Machine requires regular maintenance.");
    }

    public void operate() {
        System.out.println("Machine is operating as expected.");
    }
}