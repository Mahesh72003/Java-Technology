package com.xworkz.methodoverride8;

public class Character {

    public void speak() {
        System.out.println("Character is speaking.");
    }

    public void act() {
        System.out.println("Character is performing an action.");
    }

    public void assist() {
        System.out.println("Character is offering help.");
    }

    public void retreat() {
        System.out.println("Character is retreating from the scene.");
    }
}