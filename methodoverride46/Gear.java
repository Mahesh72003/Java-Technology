package com.xworkz.methodoverride46;

public class Gear {
    public void equip() {
        System.out.println("Gear is equipped for use.");
    }

    public void use() {
        System.out.println("Gear is being used in operation.");
    }

    public void maintain() {
        System.out.println("Gear is being maintained for longevity.");
    }

    public void store() {
        System.out.println("Gear is stored properly after use.");
    }

    public void info() {
        System.out.println("This is general outdoor or tactical gear.");
    }
}