package com.xworkz.methodoverride44;

public class Activity {
    public void start() {
        System.out.println("Activity has started.");
    }

    public void pause() {
        System.out.println("Activity is paused.");
    }

    public void resume() {
        System.out.println("Activity has resumed.");
    }

    public void stop() {
        System.out.println("Activity has ended.");
    }

    public void info() {
        System.out.println("This is a general activity.");
    }
}