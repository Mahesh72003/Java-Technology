package com.xworkz.methodoverride29;

public class Structure {

    public void foundation() {
        System.out.println("The structure has a strong foundation.");
    }

    public void walls() {
        System.out.println("The structure has solid walls.");
    }

    public void roof() {
        System.out.println("The structure has a secure roof.");
    }

    public void rooms() {
        System.out.println("The structure has multiple rooms.");
    }
}