package com.xworkz.methodoverride17;

public class Plant {

    public void grow() {
        System.out.println("Plant is growing with sunlight and water.");
    }

    public void photosynthesize() {
        System.out.println("Plant is performing photosynthesis.");
    }

    public void absorbWater() {
        System.out.println("Plant is absorbing water through its roots.");
    }

    public void reproduce() {
        System.out.println("Plant is reproducing through seeds or cuttings.");
    }
}