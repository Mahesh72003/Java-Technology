package com.xworkz.methodoverride5;

public class Furniture {

    public void assemble() {
        System.out.println("Furniture is being assembled.");
    }

    public void disassemble() {
        System.out.println("Furniture is being disassembled.");
    }

    public void polish() {
        System.out.println("Furniture is being polished.");
    }

    public void move() {
        System.out.println("Furniture is being moved to a new location.");
    }
}