package com.xworkz.methodoverride35;

public class Gadget {

    public void powerOn() {
        System.out.println("The gadget is powered on.");
    }

    public void operate() {
        System.out.println("The gadget is operating.");
    }

    public void stop() {
        System.out.println("The gadget has stopped working.");
    }

    public void store() {
        System.out.println("The gadget is stored safely.");
    }
}