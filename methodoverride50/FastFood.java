package com.xworkz.methodoverride50;

public class FastFood {
    public void order() {
        System.out.println("Fast food order is placed.");
    }

    public void prepare() {
        System.out.println("Fast food is being prepared.");
    }

    public void serve() {
        System.out.println("Fast food is served.");
    }

    public void eat() {
        System.out.println("Fast food is being eaten.");
    }

    public void info() {
        System.out.println("This is a general fast food item.");
    }
}