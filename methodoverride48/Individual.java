package com.xworkz.methodoverride48;

public class Individual {
    public void think() {
        System.out.println("Individual is thinking.");
    }

    public void speak() {
        System.out.println("Individual is speaking.");
    }

    public void work() {
        System.out.println("Individual is working.");
    }

    public void sleep() {
        System.out.println("Individual is resting.");
    }

    public void info() {
        System.out.println("This is a general individual.");
    }
}