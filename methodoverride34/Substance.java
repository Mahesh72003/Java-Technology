package com.xworkz.methodoverride34;

public class Substance {

    public void form() {
        System.out.println("The substance is in a basic form.");
    }

    public void use() {
        System.out.println("The substance is being used.");
    }

    public void dispose() {
        System.out.println("The substance is being disposed of.");
    }

    public void recycle() {
        System.out.println("The substance is being recycled.");
    }
}