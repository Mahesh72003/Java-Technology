package com.xworkz.methodoverride30;

public class Item {

    public void use() {
        System.out.println("Item is being used.");
    }

    public void clean() {
        System.out.println("Item is being cleaned.");
    }

    public void store() {
        System.out.println("Item is being stored.");
    }

    public void display() {
        System.out.println("Item is displayed.");
    }
}