package com.xworkz.methodoverride24;

public class RCAircraft {

    public void takeOff() {
        System.out.println("RC aircraft is taking off smoothly.");
    }

    public void land() {
        System.out.println("RC aircraft is landing safely.");
    }

    public void hover() {
        System.out.println("RC aircraft is hovering in the air.");
    }

    public void control() {
        System.out.println("RC aircraft is being controlled by the remote.");
    }
}