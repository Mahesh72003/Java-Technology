package com.xworkz.methodoverride38;

public class Book {

    public void read() {
        System.out.println("You are reading a book.");
    }

    public void open() {
        System.out.println("You open the book and start reading.");
    }

    public void close() {
        System.out.println("You close the book after reading.");
    }

    public void bookmark() {
        System.out.println("You bookmark the page you're currently reading.");
    }
}