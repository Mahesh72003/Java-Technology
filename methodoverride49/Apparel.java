package com.xworkz.methodoverride49;

public class Apparel {
    public void wear() {
        System.out.println("Apparel is being worn.");
    }

    public void remove() {
        System.out.println("Apparel is being removed.");
    }

    public void clean() {
        System.out.println("Apparel is being cleaned.");
    }

    public void store() {
        System.out.println("Apparel is stored after use.");
    }

    public void info() {
        System.out.println("This is a general apparel item.");
    }
}