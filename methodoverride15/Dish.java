package com.xworkz.methodoverride15;

public class Dish {

    public void prepare() {
        System.out.println("Dish is being prepared.");
    }

    public void serve() {
        System.out.println("Dish is being served to the customer.");
    }

    public void taste() {
        System.out.println("Dish has a delightful taste.");
    }

    public void cleanUp() {
        System.out.println("Dish utensils are being cleaned after use.");
    }
}