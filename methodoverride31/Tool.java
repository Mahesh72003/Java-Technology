package com.xworkz.methodoverride31;

public class Tool {

    public void useTool() {
        System.out.println("Tool is being used.");
    }

    public void storeTool() {
        System.out.println("Tool is being stored after use.");
    }

    public void maintainTool() {
        System.out.println("Tool is being maintained.");
    }

    public void displayTool() {
        System.out.println("Tool is being displayed.");
    }
}