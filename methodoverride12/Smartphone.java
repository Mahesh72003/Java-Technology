package com.xworkz.methodoverride12;

public class Smartphone extends Technology {

    @Override
    public void innovate() {
        System.out.println("Smartphone technology is introducing new features like foldable screens and 5G.");
    }

    @Override
    public void apply() {
        System.out.println("Smartphone is being used for communication, entertainment, and productivity.");
    }

    @Override
    public void develop() {
        System.out.println("Smartphone technology is being developed with AI integration and enhanced battery life.");
    }

    @Override
    public void communicate() {
        System.out.println("Smartphone is enabling instant communication through calls, texts, and apps.");
    }
}