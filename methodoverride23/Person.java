package com.xworkz.methodoverride23;

public class Person {

    public void speak() {
        System.out.println("Person is speaking about daily life.");
    }

    public void walk() {
        System.out.println("Person is walking through the park.");
    }

    public void eat() {
        System.out.println("Person is eating a meal.");
    }

    public void sleep() {
        System.out.println("Person is sleeping peacefully.");
    }
}