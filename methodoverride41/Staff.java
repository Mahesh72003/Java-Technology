package com.xworkz.methodoverride41;

public class Staff {
    public void work() {
        System.out.println("Staff is performing general duties.");
    }

    public void report() {
        System.out.println("Staff is preparing reports.");
    }

    public void attendMeeting() {
        System.out.println("Staff is attending a meeting.");
    }

    public void takeBreak() {
        System.out.println("Staff is on a break.");
    }

    public void info() {
        System.out.println("This is a general staff member.");
    }
}