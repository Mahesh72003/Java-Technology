package com.xworkz.methodoverride7;

public class Food {

    public void prepare() {
        System.out.println("Food is being prepared.");
    }

    public void cook() {
        System.out.println("Food is being cooked.");
    }

    public void serve() {
        System.out.println("Food is being served.");
    }

    public void pack() {
        System.out.println("Food is being packed for takeaway.");
    }
}