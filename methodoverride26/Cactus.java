package com.xworkz.methodoverride26;

public class Cactus {

    public void storeWater() {
        System.out.println("Cactus stores water in its thick stem.");
    }

    public void surviveDesert() {
        System.out.println("Cactus can survive harsh desert climates.");
    }

    public void photosynthesize() {
        System.out.println("Cactus performs photosynthesis through its stem.");
    }

    public void bloom() {
        System.out.println("Cactus blooms with colorful flowers occasionally.");
    }
}