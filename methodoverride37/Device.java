package com.xworkz.methodoverride37;

public class Device {

    public void powerOn() {
        System.out.println("The device is powered on.");
    }

    public void operate() {
        System.out.println("The device is operating.");
    }

    public void shutdown() {
        System.out.println("The device is shutting down.");
    }

    public void reset() {
        System.out.println("The device is being reset.");
    }
}