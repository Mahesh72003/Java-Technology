package com.xworkz.methodoverride42;

public class Sound {
    public void play() {
        System.out.println("Sound is playing.");
    }

    public void stop() {
        System.out.println("Sound has been stopped.");
    }

    public void increaseVolume() {
        System.out.println("Volume of the sound is increasing.");
    }

    public void decreaseVolume() {
        System.out.println("Volume of the sound is decreasing.");
    }

    public void info() {
        System.out.println("This is a general sound.");
    }
}