package com.xworkz.methodoverride22;

public class Toy {

    public void play() {
        System.out.println("Toy is being played with joyfully.");
    }

    public void store() {
        System.out.println("Toy is stored in the toy box.");
    }

    public void breakToy() {
        System.out.println("Toy is broken and needs repair.");
    }

    public void repair() {
        System.out.println("Toy is being repaired and will be like new.");
    }
}