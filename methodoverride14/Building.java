package com.xworkz.methodoverride14;

public class Building {

    public void construct() {
        System.out.println("Building is being constructed.");
    }

    public void renovate() {
        System.out.println("Building is being renovated.");
    }

    public void maintain() {
        System.out.println("Building is undergoing maintenance.");
    }

    public void use() {
        System.out.println("Building is being used by people for various purposes.");
    }
}