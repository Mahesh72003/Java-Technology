package com.xworkz.methodoverride47;

public class Leisure {
    public void relax() {
        System.out.println("Leisure time is being enjoyed.");
    }

    public void enjoy() {
        System.out.println("Leisure activity is fun and relaxing.");
    }

    public void pause() {
        System.out.println("Leisure activity is paused.");
    }

    public void resume() {
        System.out.println("Leisure activity is resumed.");
    }

    public void info() {
        System.out.println("This is a general leisure activity.");
    }
}