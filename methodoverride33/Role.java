package com.xworkz.methodoverride33;

public class Role {

    public void perform() {
        System.out.println("The role is being performed.");
    }

    public void grow() {
        System.out.println("The role is growing through the storyline.");
    }

    public void influence() {
        System.out.println("The role is influencing the plot.");
    }

    public void resolve() {
        System.out.println("The role is resolving the conflict in the story.");
    }
}