package com.xworkz.methodoverride4;

public class CoolingSystem {

    public void turnOn() {
        System.out.println("Cooling system is turned on.");
    }

    public void turnOff() {
        System.out.println("Cooling system is turned off.");
    }

    public void regulateTemperature() {
        System.out.println("Cooling system is regulating the temperature.");
    }

    public void autoShutdown() {
        System.out.println("Cooling system will auto shutdown after scheduled time.");
    }
}