package com.xworkz.methodoverride45;

public class Greenery {
    public void grow() {
        System.out.println("Greenery is growing naturally.");
    }

    public void trim() {
        System.out.println("Greenery is being trimmed.");
    }

    public void water() {
        System.out.println("Greenery is being watered.");
    }

    public void bloom() {
        System.out.println("Greenery is blooming beautifully.");
    }

    public void info() {
        System.out.println("This is a general type of greenery.");
    }
}