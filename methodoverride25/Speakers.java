package com.xworkz.methodoverride25;

public class Speakers {

    public void playSound() {
        System.out.println("Speakers are playing music loudly.");
    }

    public void increaseVolume() {
        System.out.println("Speakers volume increased.");
    }

    public void decreaseVolume() {
        System.out.println("Speakers volume decreased.");
    }

    public void mute() {
        System.out.println("Speakers are muted.");
    }
}