package com.xworkz.methodoverride36;

public class Smartwatch extends Innovation {

    @Override
    public void design() {
        System.out.println("The smartwatch has been designed with a sleek interface.");
    }

    @Override
    public void function() {
        System.out.println("The smartwatch tracks health metrics and notifications.");
    }

    @Override
    public void improve() {
        System.out.println("The smartwatch is improved with new features like GPS and better battery life.");
    }

    @Override
    public void release() {
        System.out.println("The smartwatch is released with updates and app support.");
    }
}