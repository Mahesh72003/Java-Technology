package com.xworkz.methodoverride21;

public class Electronics {

    public void turnOn() {
        System.out.println("Electronics device is now turned on.");
    }

    public void turnOff() {
        System.out.println("Electronics device is turned off.");
    }

    public void reset() {
        System.out.println("Electronics device is resetting to default settings.");
    }

    public void update() {
        System.out.println("Electronics device is checking for updates.");
    }
}