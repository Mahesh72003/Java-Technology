package com.xworkz.methodoverride32;

public class Dish {

    public void prepare() {
        System.out.println("Dish is being prepared.");
    }

    public void serve() {
        System.out.println("Dish is served on the table.");
    }

    public void eat() {
        System.out.println("Dish is being eaten.");
    }

    public void cleanUp() {
        System.out.println("Cleaning up after the dish.");
    }
}