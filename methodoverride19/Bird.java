package com.xworkz.methodoverride19;

public class Bird {

    public void fly() {
        System.out.println("Bird is flying high in the sky.");
    }

    public void sing() {
        System.out.println("Bird is singing a beautiful melody.");
    }

    public void eat() {
        System.out.println("Bird is pecking at seeds and insects.");
    }

    public void sleep() {
        System.out.println("Bird is sleeping quietly in its nest.");
    }
}